
//  ODD OR EVEN
public class CF1 {
    public static void main(String[] args) {
        int n;
        for (int i = 1; i <= 50; i++) {

            if (i % 2 == 0) {
                System.out.print(i);
            }
        }
        for (int i = 1; i <= 50; i++) {

            if (i % 2 != 0) {
                System.out.println(i);
            }
        }
    }
}
